package honja;

public class Oper {
    
    public static int add(int x, int y) {
        int result = x + y;
        return result;
    }
    
    public static int minus(int x, int y) {
        int result = x - y;
        return result;
    }
    
    public static int mul(int x, int y) {
        int result = x * y;
        return result;
    }
    
    public static double div(int x, int y) {
        double result = (double)x / y;
        return result;
    }
}
